@Dao
interface CacheDao {

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    fun addCache(vararg item: CacheBean)

    @Update
    fun updateCache(vararg item: CacheBean)

    @Delete
    fun removeCache(vararg item: CacheBean)

    @Query("delete  from cache where _cache_key=:cacheKey")
    fun removeCacheByKey(cacheKey: String)

    @Query("select * from cache where _cache_key =:cacheKey")
    fun loadCache(cacheKey: String): List<CacheBean>
}