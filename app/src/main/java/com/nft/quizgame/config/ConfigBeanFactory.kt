object ConfigBeanFactory {
    fun getConfigBean(sid: Int): AbsConfigBean? {
        val cacheOutDateListener = object : AbsConfigBean.CacheListener{
            override fun onConfigDataOutDate() {
                ConfigManager.getInstance().requestConfig(QuizAppState.getContext(), sid)
            }
        }
        return when (sid) {
            AdConfigBean.SID -> AdConfigBean(
                    cacheOutDateListener)
            ExternalDialogAdConfigBean.SID -> ExternalDialogAdConfigBean(
                    cacheOutDateListener)
            else -> null
        }
    }
}